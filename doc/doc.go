// Packge dwolla is a third party client library for Dwolla REST API.
//
// The package provides APIs and utilites to be used do develop Go applications
// with Dwolla payment services such as customers, transfers, masspayments , ...etc.
//
// The library removes the complexity of calling the endpoints directly from the application.
// It handles some things like creating and maintaining the dwolla access token that expires 
// after one hour.
//
// For more info and to understand Dwolla payment services.
// https://docs.dwolla.com/